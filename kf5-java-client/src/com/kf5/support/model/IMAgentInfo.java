package com.kf5.support.model;

import java.util.List;

public class IMAgentInfo {

	private List<IMGroup> imGroups;

	private List<IMAgent> imAgents;

	public List<IMGroup> getImGroups() {
		return imGroups;
	}

	public void setImGroups(List<IMGroup> imGroups) {
		this.imGroups = imGroups;
	}

	public List<IMAgent> getImAgents() {
		return imAgents;
	}

	public void setImAgents(List<IMAgent> imAgents) {
		this.imAgents = imAgents;
	}

}
